int main()
{	
	ll x;
	cin>>x;
	vector<pll> V;
	for(ll i=1;i*i<=x;i++)
	{
		if(x%i==0)
		{
			V.push_back({i,x/i});
		}
	}
	ll maa=x;
	ll ans1,ans2;
	for(auto x:V)
	{
		if(__gcd(x.first,x.second)==1)
		{
			if(maa>=max(x.first,x.second))
			{
				ans1=x.first,ans2=x.second;
			}
			maa=min(maa,max(x.first,x.second));
		}
	}
	cout<<ans1<<" "<<ans2<<endl;
}